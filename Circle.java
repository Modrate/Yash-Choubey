public class Circle {
    //This class is going to be used in main class

    double x, y; // The coordinates of center
    double r; // The radius

    // Methods that returns circumference
    double circum() {
        return 2 * 3.14 * r;
    }
    //method that returns area

    double area() {
        return (22 / 7) * r * r;
    }

}
